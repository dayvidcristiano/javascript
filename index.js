function somar(a, b) {
  let soma = a + b
  return soma
}
console.log("Soma: " + somar(2, 3))

function multi(a, b) {
  let multip = a * b
  return multip
}
console.log("Multiplicação: " + multi(2, 3))

function subt(a, b) {
  let subtr = a - b
  return subtr
}
console.log("Subtração: " + subt(10, 2))

function div(a, b) {
  let divi = a / b
  return divi
}